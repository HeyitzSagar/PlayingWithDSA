// DOM MANIPULATION

// GetElementById()
var title = document.getElementById("Main-Heading");
console.log(title);

// GetElementByClassName()

var classname = document.getElementsByClassName("list-item");
console.log(classname);
// GetElementByTagName()

var tagName = document.getElementsByTagName("h1");
console.log(tagName);
// querySelector()

var querySelector = document.querySelector("div");
console.log(querySelector);
// querySelectorAll()
//  to select all the div or document use queryselectorAll
// this method return an NodeList
var querySelectorAll = document.querySelectorAll("div");
console.log(querySelectorAll);


